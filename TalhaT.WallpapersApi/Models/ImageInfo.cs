namespace TalhaT.WallpapersApi.Models
{
    public class ImageInfo
    {
        /// <summary>
        /// File name of image
        /// </summary>
        public string FileName { get; set; }

        /// <summary>
        /// Width of image
        /// </summary>
        public int Width { get; set; }

        /// <summary>
        /// Height of image
        /// </summary>
        public int Height { get; set; }

        /// <summary>
        /// Link to the image
        /// </summary>
        public string Link { get; set; }
    }
}